let currentLanguage = 'ar';

function detectEmotion(text, language) {

  

  if (!responses[language] || !keywords[language]) {

    language = 'en';

  }

  

  const textLower = text.toLowerCase();

  let detectedEmotion = null;

  let maxMatches = 0;

  for (const [emotion, words] of Object.entries(keywords[language])) {

    let matches = 0;

    for (const word of words) {

      if (textLower.includes(word)) {

        matches++;

      }

    }

    if (matches > maxMatches) {

      maxMatches = matches;

      detectedEmotion = emotion;

    }

  }

  if (!detectedEmotion || maxMatches === 0) {

    const emotions = Object.keys(responses[language]);

    const normalEmotions = emotions.filter(e => 

      !['sadness', 'happiness', 'anger'].includes(e)

    );

    

    if (normalEmotions.length > 0) {

      detectedEmotion = normalEmotions[Math.floor(Math.random() * normalEmotions.length)];

    } else {

      detectedEmotion = emotions[Math.floor(Math.random() * emotions.length)];

    }

  }

  return detectedEmotion;

}

function sendMessage() {

  const userInput = document.getElementById('user-input').value.trim();

  if (!userInput) return;

  const chatContainer = document.getElementById('chat-container');

  

  const userMsg = document.createElement('div');

  userMsg.className = 'message user-message';

  userMsg.textContent = ` ${userInput}`;

  chatContainer.appendChild(userMsg);

  document.getElementById('user-input').value = '';

  chatContainer.scrollTop = chatContainer.scrollHeight;



  const typingIndicator = document.getElementById('typing-indicator');

  typingIndicator.style.display = 'block';

  chatContainer.scrollTop = chatContainer.scrollHeight;

  

  const typingTime = Math.min(3000, Math.max(1000, userInput.length * 50));

  

  setTimeout(() => {

    const emotion = detectEmotion(userInput, currentLanguage);

    let possibleResponses = responses[currentLanguage][emotion] || responses[currentLanguage]['greeting'];

    

    

    let filteredResponses = possibleResponses;

    if (userInput.length < 10) {

      

      filteredResponses = possibleResponses.filter(r => r.length < 50);

    } else if (userInput.length > 50) {

      

      filteredResponses = possibleResponses.filter(r => r.length > 70);

    } else {

      

      filteredResponses = possibleResponses.filter(r => r.length >= 50 && r.length <= 70);

    }

    

    

    if (filteredResponses.length === 0) {

      filteredResponses = possibleResponses;

    }

    

    const randomResponse = filteredResponses[Math.floor(Math.random() * filteredResponses.length)];

    

    typingIndicator.style.display = 'none';

    

    const botMsg = document.createElement('div');

    botMsg.className = 'message bot-message';

    botMsg.textContent = ` ${randomResponse}`;

    

    const emotionTag = document.createElement('div');

    emotionTag.className = 'emotion-tag';

    emotionTag.textContent = emotion;

    botMsg.appendChild(emotionTag);

    

    chatContainer.appendChild(botMsg);

    chatContainer.scrollTop = chatContainer.scrollHeight;

  }, typingTime);

}

